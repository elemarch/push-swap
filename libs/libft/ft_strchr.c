#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;

	i = 0;
	while (str[i] != c && str[i])
		i++;
	if (str[i] == '\0' && c != 0)
		return (NULL);
	return ((char*)&str[i]);
}
