#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		size;
	int		i;

	i = 0;
	size = ft_strlen(str);
	dup = (char*)malloc(sizeof(char) * size + 1);
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
